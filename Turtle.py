
import turtle

maxCounter = 1
counter = 0
myTurtle = turtle.Turtle()

while (counter < maxCounter):
    myTurtle.right(45)
    myTurtle.forward(105)
    myTurtle.right(135)
    myTurtle.forward(75)
    myTurtle.right(90)
    myTurtle.forward(75)
    myTurtle.right(90)
    myTurtle.forward(75)
    myTurtle.right(90)
    myTurtle.forward(75)


    counter = counter + 1

turtle.done()
